/**
  * @brief This function handles DMA1 stream6 global interrupt.
  */
void DMA1_Stream6_IRQHandler(void)
{

	UINT wbytes;
	uint32_t tmpisr;
	DMA_Base_Registers *regs = (DMA_Base_Registers *)hdma_dac2.StreamBaseAddress;

	tmpisr = regs->ISR;

	/* Half Transfer Complete Interrupt management ******************************/
	if ((tmpisr & (DMA_FLAG_HTIF0_4 << hdma_dac2.StreamIndex)) != RESET)
	{
		//f_read(&SoundFile,(char*)Dac_Buffer,DAC_BUFFER_SIZE/2,&wbytes);
		for(int i=0;i<DAC_BUFFER_SIZE/2;i++)
			*(Dac_Buffer+i) = *(Dac_Temp_Buffer+i);

	}
	/* Transfer Complete Interrupt management ***********************************/
	if ((tmpisr & (DMA_FLAG_TCIF0_4 << hdma_dac2.StreamIndex)) != RESET)
	{
		//f_read(&SoundFile,(char*)(Dac_Buffer+(DAC_BUFFER_SIZE/2)),DAC_BUFFER_SIZE/2,&wbytes);
		for(int i=DAC_BUFFER_SIZE/2;i<DAC_BUFFER_SIZE;i++)
			*(Dac_Buffer+i) = *(Dac_Temp_Buffer+i);

		DAC_Status=1;
	}

	HAL_DMA_IRQHandler(&hdma_dac2);

}

